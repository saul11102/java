    public void limpiar(){
        txtNombre.setText("");
        txtFoto.setText("");
        txtCedula.setText("");
        cbxPartido.setSelectedItem(0);
        cbxDignidad.setSelectedItem(0);
        cargarTabla();
        cargarCombo();
    }
    
    private void cargarCombo() {
        try {
            CargarCombo.cargarPartidoPolitico(cbxPartido, ppd);
            CargarCombo.cargarDignidad(cbxDignidad, dd);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

